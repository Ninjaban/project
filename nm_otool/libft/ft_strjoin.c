#include "libft.h"
#include <string.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*tmp;

	if (s1 == NULL && s2 == NULL)
		return (NULL);
	if (s1 == NULL)
		return (ft_strdup(s2));
	if (s2 == NULL)
		return (ft_strdup(s1));
	tmp = ft_strnew(ft_strlen(s1) + ft_strlen(s2));
	return (ft_strcat(ft_strcpy(tmp, s1), s2));
}
